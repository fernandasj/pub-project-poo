    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        
        String nome = jTextField1.getText();
        String cpf = jFormattedTextField1.getText();
        LocalDate nascimento = LocalDate.parse(jFormattedTextField2.getText(),
                formatter);
        String telefone = jFormattedTextField3.getText();
        String senha = new String(jPasswordField1.getPassword());
        String cargo = (String) jComboBox1.getSelectedItem();
        float salario = (float) jSpinner1.getValue();
        LocalDate admissao = LocalDate.parse(jFormattedTextField4.getText(),
                formatter);
        
        Funcionario funcionario = new Funcionario(salario, admissao, cargo,
                senha, cpf, nome, telefone, nascimento);
        
        try {
            if(dao.buscar(cpf) == null){
                if(dao.salvar(funcionario)){
                    JOptionPane.showMessageDialog(null, "Salvo com sucesso");
                    TelaCadastroFuncionario cadastroFuncionario = new TelaCadastroFuncionario();
                    cadastroFuncionario.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Falha ao salvar");
                }
            } else {
                JOptionPane.showMessageDialog(null, "O funcionario j√° foi cadastrado");
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Falha na leitura dos dados");
            this.dispose();
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Classe n√£o encontrada");
            this.dispose();
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

